package src.javaEngineer.aula13;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int valueOne = 3;
		int valueTwo = 5;
		
		System.out.println("valor1 == valor2: " + (valueOne == valueTwo));
		System.out.println("valor1 != valor2: " + (valueOne != valueTwo));
		System.out.println("valor1 < valor2: " + (valueOne < valueTwo));
		System.out.println("valor1 > valor2: " + (valueOne > valueTwo));
		System.out.println("valor1 <= valor2: " + (valueOne <= valueTwo));
		System.out.println("valor1 >= valor2: " + (valueOne >= valueTwo));

	}

}
